             public void editar () 
                     
             {
                 try {
                      
                     String SQL= "update alumnos set nombre=?, apeliido=?, materia=?, calificacion=? where idalumnos =?";
                     int filaSelect =jTableAlumnos.getSelectedRow();
                     String id = (String ) jTableAlumnos.getValueAt(filaSelect, 0);
                     
                     PreparedStatement p = cn.prepareStatement(SQL);
                     
                     p.setString(1, txtNombre.getText());
                     p.setString(2, txtApellido.getText());
                     p.setString(3, txtCurso.getText());
                     p.setString(4, txtCalificacion.getText());
                     p.setString(5, id);
                     p.execute();
                     
                    JOptionPane.showMessageDialog(null, "Registro Actualizado Exitosamente");
                 }
                 
                 catch (Exception e)
                 {
                     JOptionPane.showMessageDialog(null, "Error al registrar"+e.getMessage());
                 }
                 
             }
       
             public void MostrarDatos () {
                 String [] titulos = { "ID","Nombre","Apeliido","Materia","Calificacion"};
                 String [] registros = new String [7];
                 DefaultTableModel modelo = new  DefaultTableModel (null, titulos);
                 String sql = "select * from alumnos";
                 
                 try {
                     Statement st = cn.createStatement();
                     ResultSet rs =st.executeQuery(sql);
                     while (rs.next()){
                         registros [0]=rs.getString("idalumnos");
                         registros [1]=rs.getString("Nombre");
                         registros [2]=rs.getString("Apeliido");
                         registros [3]=rs.getString("materia");
                         registros [4]=rs.getString("Calificacion");
                         modelo.addRow(registros);
                         
                     }
                     jTableAlumnos.setModel(modelo);
                 }
                 catch  (Exception e) {
                     JOptionPane.showMessageDialog(null, "ERROR AL MOSTRAR DATOS"+e.getMessage());
                     
                 }
             }
       
             
             public void eliminar (){
                  if(JOptionPane.showConfirmDialog(this,"eliminar registro","confirmar",JOptionPane.YES_NO_OPTION)==0)
        
         
         {      
         
             String  sSql= "Delete from alumnos where idalumnos =?";
             
             
             int filaSelect =jTableAlumnos.getSelectedRow();
                     String id = (String ) jTableAlumnos.getValueAt(filaSelect, 0);
             try
             {
                 PreparedStatement p = cn.prepareStatement(sSql);
                 p.setString(1, id);
                 
                 p.executeUpdate();
                 
                 JOptionPane.showMessageDialog(null, "El registro ha sido eliminado exitosamente",
                         "Operación Exitosa", JOptionPane.INFORMATION_MESSAGE);
             }
             
            catch (Exception e)
                 {
                     JOptionPane.showMessageDialog(null, "Error al eliminar"+e.getMessage());
                 }
                 
             }
             }
             
             
             

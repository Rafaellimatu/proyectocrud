             public void insertarDatos (){
            try
            {
                
            
                String sSql="insert into alumnos (nombre,apeliido,materia,calificacion) values (?,?,?,?)";
                
                  PreparedStatement p = cn.prepareStatement(sSql);
                  p.setString(1, txtNombre.getText());
                  p.setString(2, txtApellido.getText());
                  p.setString(3, txtCurso.getText());
                  p.setString(4, txtCalificacion.getText());
                  String home = txtCalificacion.getText();
                if(home.length()<101)
                {
                  p.executeUpdate();
                  
                  JOptionPane.showMessageDialog(this, "los datos se Guardaron exitosamente");
                
                  
                }
                
                else 
                {
                    JOptionPane.showMessageDialog(this, "ERROR");
                }
            }
            
            catch (Exception e) {
            JOptionPane.showMessageDialog(this, "No se pudo ingresar los dialogos");
             JOptionPane.showMessageDialog(null,e.getMessage());
                    
        }
        
        }
               
    
